def _latex_escape(value: Any) -> str:
    text = "" if value is None else str(value)
    replacements = {
        "\\": r"\textbackslash{}",
        "&": r"\&",
        "%": r"\%",
        "$": r"\$",
        "#": r"\#",
        "_": r"\_",
        "{": r"\{",
        "}": r"\}",
        "~": r"\textasciitilde{}",
        "^": r"\textasciicircum{}",
    }
    return "".join(replacements.get(char, char) for char in text)


def _safe_float(value: Any) -> float | None:
    try:
        if value is None or value == "":
            return None
        out = float(value)
    except (TypeError, ValueError):
        return None
    if not math.isfinite(out):
        return None
    return out


def _format_float(value: Any, ndigits: int = 3) -> str:
    out = _safe_float(value)
    if out is None:
        return "--"
    return f"{out:.{ndigits}f}".rstrip("0").rstrip(".")


def _value_column(frame: pd.DataFrame) -> str:
    if "Feq_MN" in frame.columns:
        return "Feq_MN"
    if "Feq" in frame.columns:
        return "Feq"
    raise KeyError("SRS curve must contain Feq_MN or Feq")


def _load_curve_frame(record: dict[str, Any]) -> pd.DataFrame:
    frame = pd.read_csv(record["curve_csv_path"])
    for key, value in record.items():
        if isinstance(value, (dict, list)):
            continue
        if key not in frame.columns:
            frame.insert(0, key, value)
    return frame


def _curve_label(record: dict[str, Any]) -> str:
    mode = record.get("meta_mode") or record.get("scenario_name") or "scenario"
    speed = _format_float(record.get("meta_speed_kmh"), 0)
    contact = record.get("meta_contact_model") or "contact=?"
    mu = _format_float(record.get("meta_mu"), 2)
    zeta = _format_float(record.get("zeta"), 3)
    return f"{mode}, v={speed} km/h, {contact}, mu={mu}, zeta={zeta}"


def _plot_curves(path: Path, labelled_frames: Iterable[tuple[str, pd.DataFrame]], *, title: str, ylabel: str) -> Path:
    fig, ax = plt.subplots(figsize=(6.8, 4.2))
    any_curve = False
    for label, frame in labelled_frames:
        if "Tn_ms" not in frame.columns or ylabel not in frame.columns:
            continue
        ax.plot(frame["Tn_ms"], frame[ylabel], label=label)
        any_curve = True
    ax.set_xscale("log")
    ax.set_xlabel(r"$T_n$ [ms]")
    ax.set_ylabel(ylabel.replace("_", r"\_"))
    ax.set_title(title)
    ax.grid(True, which="both", alpha=0.35)
    if any_curve:
        ax.legend(fontsize="x-small")
    fig.tight_layout()
    path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(path, dpi=180)
    plt.close(fig)
    return path


def _write_csv_latex_table(path: Path, frame: pd.DataFrame, *, caption: str, label: str, max_rows: int = 12) -> Path:
    shown = frame.head(max_rows).copy()
    lines = [
        r"\begin{table}[htbp]",
        r"\centering",
        r"\small",
        rf"\caption{{{_latex_escape(caption)}}}",
        rf"\label{{{label}}}",
        r"\begin{tabular}{" + "l" * max(1, len(shown.columns)) + "}",
        r"\toprule",
        " & ".join(_latex_escape(c) for c in shown.columns) + r" \\",
        r"\midrule",
    ]
    for _, row in shown.iterrows():
        values = []
        for value in row.tolist():
            if isinstance(value, float):
                values.append(_format_float(value, 3))
            else:
                values.append(_latex_escape(value))
        lines.append(" & ".join(values) + r" \\")
    if len(frame) > len(shown):
        lines.append(rf"\multicolumn{{{len(shown.columns)}}}{{l}}{{\emph{{Table truncated; see CSV artifact for all rows.}}}} \\")
    lines.extend([r"\bottomrule", r"\end{tabular}", r"\end{table}", ""])
    path.write_text("\n".join(lines), encoding="utf-8")
    return path


