def _make_summary_tables(
    *,
    output_dir: Path,
    run_records: list[dict[str, Any]],
    curve_frames: list[pd.DataFrame],
) -> tuple[list[Path], dict[str, Any]]:
    tables_dir = output_dir / "tables"
    tables_dir.mkdir(parents=True, exist_ok=True)
    tables: list[Path] = []
    summary: dict[str, Any] = {}

    run_frame = pd.DataFrame(run_records)
    if not run_frame.empty:
        cols = [c for c in ["meta_vehicle", "meta_mode", "meta_speed_kmh", "meta_contact_model", "meta_mu", "run_status", "elapsed_s"] if c in run_frame.columns]
        short = run_frame[cols].copy() if cols else run_frame.copy()
        short.to_csv(tables_dir / "run_summary.csv", index=False)
        tables.append(_write_csv_latex_table(tables_dir / "run_summary.tex", short, caption="Persisted simulation runs used by the chapter generator.", label="tab:run-summary"))
        summary["n_runs"] = int(len(run_frame))
        summary["n_ok_runs"] = int((run_frame.get("run_status") == "ok").sum()) if "run_status" in run_frame else None
        if "meta_mode" in run_frame:
            summary["modes"] = sorted(str(v) for v in run_frame["meta_mode"].dropna().unique())
        if "meta_contact_model" in run_frame:
            summary["contact_models"] = sorted(str(v) for v in run_frame["meta_contact_model"].dropna().unique())

    if curve_frames:
        long = pd.concat(curve_frames, ignore_index=True)
        long.to_csv(tables_dir / "srs_curves_long.csv", index=False)
        summary["n_srs_points"] = int(len(long))
        summary["n_srs_curves"] = int(len(curve_frames))
        valcol = _value_column(curve_frames[0])
        maxima_cols = [c for c in ["scenario_name", "meta_vehicle", "meta_mode", "meta_speed_kmh", "meta_contact_model", "meta_mu", "zeta"] if c in long.columns]
        if maxima_cols:
            maxima = long.groupby(maxima_cols, dropna=False)[valcol].max().reset_index().sort_values(valcol, ascending=False)
            maxima.to_csv(tables_dir / "srs_maxima.csv", index=False)
            tables.append(_write_csv_latex_table(tables_dir / "srs_maxima.tex", maxima, caption="Maximum equivalent SRS force per persisted curve.", label="tab:srs-maxima"))
            governing = maxima.iloc[0].to_dict() if not maxima.empty else {}
            summary["governing_srs"] = governing
    return tables, summary


def _make_figures(output_dir: Path, records: list[dict[str, Any]], curve_frames: list[pd.DataFrame], *, zeta: float | None) -> tuple[list[Path], dict[str, Any]]:
    figures_dir = output_dir / "figures"
    figures_dir.mkdir(parents=True, exist_ok=True)
    figures: list[Path] = []
    metadata: dict[str, Any] = {}
    if not records or not curve_frames:
        return figures, metadata

    filtered: list[tuple[dict[str, Any], pd.DataFrame]] = []
    for record, frame in zip(records, curve_frames):
        if zeta is not None and abs(float(record.get("zeta", np.nan)) - float(zeta)) > 1.0e-12:
            continue
        filtered.append((record, frame))
    if not filtered:
        filtered = list(zip(records, curve_frames))

    value_col = _value_column(filtered[0][1])
    labelled = [(_curve_label(record), frame) for record, frame in filtered]
    figures.append(_plot_curves(figures_dir / "srs_overlay.png", labelled, title="Persisted SRS curves", ylabel=value_col))

    envelope = SpectrumService.envelope([frame for _, frame in filtered], value_column=value_col)
    envelope_path = figures_dir / "srs_envelope.csv"
    envelope.to_csv(envelope_path, index=False)
    env_col = f"{value_col}_envelope"
    figures.append(_plot_curves(figures_dir / "srs_envelope.png", [("envelope", envelope)], title="SRS envelope", ylabel=env_col))
    metadata["envelope_csv"] = str(envelope_path)

    ratios: list[pd.DataFrame] = []
    groups: dict[tuple[Any, Any, Any, Any, Any], dict[str, pd.DataFrame]] = {}
    for record, frame in filtered:
        key = (
            record.get("meta_vehicle"),
            record.get("meta_speed_kmh"),
            record.get("meta_contact_model"),
            record.get("meta_mu"),
            record.get("zeta"),
        )
        mode = str(record.get("meta_mode"))
        groups.setdefault(key, {})[mode] = frame
    ratio_labelled: list[tuple[str, pd.DataFrame]] = []
    for key, pair in groups.items():
        if "zug_full" not in pair or "lok_solo" not in pair:
            continue
        ratio = SpectrumService.ratio(pair["zug_full"], pair["lok_solo"], value_column=value_col)
        ratio.insert(0, "vehicle", key[0])
        ratio.insert(1, "speed_kmh", key[1])
        ratio.insert(2, "contact_model", key[2])
        ratio.insert(3, "mu", key[3])
        ratio.insert(4, "zeta", key[4])
        ratios.append(ratio)
        ratio_labelled.append((f"{key[0]}, v={key[1]} km/h, {key[2]}, mu={key[3]}", ratio))
    if ratios:
        ratio_df = pd.concat(ratios, ignore_index=True)
        ratio_csv = figures_dir / "srs_full_train_vs_lok_ratio.csv"
        ratio_df.to_csv(ratio_csv, index=False)
        ratio_col = f"{value_col}_ratio"
        figures.append(_plot_curves(figures_dir / "srs_full_train_vs_lok_ratio.png", ratio_labelled, title="Full train / Lok solo SRS ratio", ylabel=ratio_col))
        metadata["ratio_csv"] = str(ratio_csv)
    return figures, metadata


